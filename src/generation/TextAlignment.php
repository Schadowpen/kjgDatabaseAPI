<?php


namespace generation;

/**
 * Ausrichtung des Textes
 * @package templates
 */
class TextAlignment
{
    /**
     * Linksbündige Ausrichtung, Position stellt den Startpunkt des Textes dar
     */
    public const alignLeft = 0;
    /**
     * Zentrierte Ausrichtung, Position stellt den Mittelpunkt des Textes dar
     */
    public const alignCenter = 1;
    /**
     * Rechtsbündige Ausrichtung, Position stellt den Endpunkt des Textes dar
     */
    public const alignRight = 2;
}